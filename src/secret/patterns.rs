/// Returns the count of embedded rules
#[allow(dead_code)]
pub fn rule_count() -> usize {
    load_embedded_rules().len()
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_load_embedded_rules() {
        let rules = load_embedded_rules();
        assert!(
            rules.len() >= 180,
            "Should have at least 180 rules, got {}",
            rules.len()
        );
    }

    #[test]
    fn test_rules_have_required_fields() {
        let rules = load_embedded_rules();
        for rule in &rules {
            assert!(!rule.rule_id.is_empty(), "rule_id should not be empty");
            assert!(!rule.title.is_empty(), "title should not be empty");
            assert!(!rule.patterns.is_empty(), "patterns should not be empty");
            assert!(!rule.provider.is_empty(), "provider should not be empty");
        }
    }

    #[test]
    fn test_rules_have_valid_severity() {
        let rules = load_embedded_rules();
        let valid_severities = ["low", "medium", "high", "critical"];
        for rule in &rules {
            assert!(
                valid_severities.contains(&rule.severity.as_str()),
                "Invalid severity: {}",
                rule.severity
            );
        }
    }

    #[test]
    fn test_aws_rules_present() {
        let rules = load_embedded_rules();
        let aws_rules: Vec<_> = rules.iter().filter(|r| r.provider == "aws").collect();
        assert!(!aws_rules.is_empty(), "Should have AWS rules");
    }

    #[test]
    fn test_github_rules_present() {
        let rules = load_embedded_rules();
        let github_rules: Vec<_> = rules.iter().filter(|r| r.provider == "github").collect();
        assert!(!github_rules.is_empty(), "Should have GitHub rules");
    }

    #[test]
    fn test_crypto_rules_present() {
        let rules = load_embedded_rules();
        let crypto_rules: Vec<_> = rules.iter().filter(|r| r.provider == "crypto").collect();
        assert!(!crypto_rules.is_empty(), "Should have crypto rules");
    }
}
